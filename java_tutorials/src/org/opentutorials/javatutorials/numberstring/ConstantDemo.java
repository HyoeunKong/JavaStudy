package org.opentutorials.javatutorials.numberstring;

public class ConstantDemo {

	public static void main(String[] args) {
		float a = 2.2F;
		long b = 21409890823L;
		byte c = 100;
		short d = 200;
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		
		int A = 3;
		float B = 1.0F;
		double C = A+ B;
		System.out.println(C);
	}

}
